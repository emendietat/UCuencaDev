# se requiere calcular el perímetro de un círculo, para ello se crearán las variables y operaciones necesarias

# variables:
r = 2  # radio
pi = 3.14159265359

# calculo del perímetro
perimetro = 2 * pi * r

# mostrar los resultados en pantalla
print(perimetro)

